import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler, OneHotEncoder
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.impute import SimpleImputer

# --- Config ---
FILE = "dateWr.xlsx"       # súbelo en Colab (o cambia la ruta)
SHEET = "Hoja1"

# --- Carga ---
df = pd.read_excel(FILE, sheet_name=SHEET)
df.columns = [c.strip().replace(" ", "_").replace("-", "_").replace(".", "") for c in df.columns]

# Asegurar datetime
df["DATE"] = pd.to_datetime(df["DATE"], errors="coerce")

# Coordenadas si existen (limpiar símbolo ° y a float)
for col in ["LATITUD_N","LONGITUD_O"]:
    if col in df.columns:
        df[col] = df[col].astype(str).str.replace("°","", regex=False)
        df[col] = pd.to_numeric(df[col], errors="coerce")
# Si LONGITUD_O es oeste, déjala negativa (si ya viene negativa, no cambia)
if "LONGITUD_O" in df.columns:
    df["LONGITUD_O"] = df["LONGITUD_O"].apply(lambda x: -abs(x) if pd.notna(x) else x)

# Variables de tiempo
df["month"] = df["DATE"].dt.month
df["dow"]   = df["DATE"].dt.dayofweek

# Elimina filas sin target ni PM_10
df = df.dropna(subset=["PM_25","PM_10"]).copy()

# Split temporal simple (80/20 por orden cronológico)
df = df.sort_values("DATE").reset_index(drop=True)
split = int(len(df)*0.8)
train_df, test_df = df.iloc[:split].copy(), df.iloc[split:].copy()

# Definir columnas numéricas/categóricas disponibles
num_cols = [c for c in ["PM_10","Temp_C","Presion__mmHg","LATITUD_N","LONGITUD_O","month","dow"] if c in df.columns]
cat_cols = [c for c in ["STATION"] if c in df.columns]

X_train = train_df[num_cols + cat_cols]
y_train = train_df["PM_25"]
X_test  = test_df[num_cols + cat_cols]
y_test  = test_df["PM_25"]

# Preprocesamiento común
from sklearn.preprocessing import StandardScaler, OneHotEncoder
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.impute import SimpleImputer

numeric_tf = Pipeline(steps=[
    ("imp", SimpleImputer(strategy="median")),
    ("scaler", StandardScaler())
])
categorical_tf = Pipeline(steps=[
    ("imp", SimpleImputer(strategy="most_frequent")),
    ("oh",  OneHotEncoder(handle_unknown="ignore"))
])

preprocess = ColumnTransformer(
    transformers=[
        ("num", numeric_tf, num_cols),
        ("cat", categorical_tf, cat_cols)
])

df.head()

"""**REGRESIÓN: Lineal vs RandomForest vs XGBoost**"""

from sklearn.linear_model import LinearRegression
from sklearn.ensemble import RandomForestRegressor
from xgboost import XGBRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

def eval_reg(y_true, y_pred, nombre="Modelo"):
    mae  = mean_absolute_error(y_true, y_pred)
    rmse = mean_squared_error(y_true, y_pred) # Removed squared=False
    r2   = r2_score(y_true, y_pred)
    print(f"{nombre} -> MAE: {mae:.3f} | RMSE: {rmse:.3f} | R²: {r2:.3f}")
    return mae, rmse, r2

"""**Regresión Lineal**"""

lin_pipe = Pipeline(steps=[("prep", preprocess),
                          ("model", LinearRegression())])
lin_pipe.fit(X_train, y_train)
pred_lin = lin_pipe.predict(X_test)
eval_reg(y_test, pred_lin, "Regresión Lineal")

lin_pipe = Pipeline(steps=[("prep", preprocess),
                          ("model", LinearRegression())])
lin_pipe.fit(X_train, y_train)
pred_lin = lin_pipe.predict(X_test)
eval_reg(y_test, pred_lin, "Regresión Lineal")

"""**Random Forest Regression**"""

rf_pipe = Pipeline(steps=[("prep", preprocess),
                         ("model", RandomForestRegressor(random_state=42))])
rf_pipe.fit(X_train, y_train)
pred_rf = rf_pipe.predict(X_test)
eval_reg(y_test, pred_rf, "Random Forest")

"""**XGBoost Regression**"""

xgb_pipe = Pipeline(steps=[("prep", preprocess),
                          ("model", XGBRegressor(random_state=42))])
xgb_pipe.fit(X_train, y_train)
pred_xgb = xgb_pipe.predict(X_test)
eval_reg(y_test, pred_xgb, "XGBoost")

"""**Comparison of Models**"""

