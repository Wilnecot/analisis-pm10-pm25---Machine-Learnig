"""**CLASIFICACIÓN: binaria (OMS 15 µg/m³) y multiclase (≤15, 15–25, >25)**"""

from sklearn.linear_model import LogisticRegression
from sklearn.svm import SVC
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, confusion_matrix, ConfusionMatrixDisplay

"""**Etiqueta BINARIA (OMS 2021 24h: 15 µg/m³)**"""

train_df["pm25_alert"] = (train_df["PM_25"] > 15).astype(int)
test_df["pm25_alert"]  = (test_df["PM_25"]  > 15).astype(int)

Xc_train = train_df[num_cols + cat_cols]
yc_train = train_df["pm25_alert"]
Xc_test  = test_df[num_cols + cat_cols]
yc_test  = test_df["pm25_alert"]

clf_lr = Pipeline(steps=[("prep", preprocess),
                        ("model", LogisticRegression(max_iter=2000, class_weight="balanced"))])
clf_lr.fit(Xc_train, yc_train)
pred_lr = clf_lr.predict(Xc_test)

print("=== Clasificación binaria (OMS 15 µg/m³) - Regresión Logística ===")
print(classification_report(yc_test, pred_lr, digits=3))
ConfusionMatrixDisplay(confusion_matrix(yc_test, pred_lr)).plot()
plt.title("Matriz de confusión - Binaria (LR)")
plt.show()

"""**Random Forest Classifier (binaria)**"""

clf_rf = Pipeline(steps=[("prep", preprocess),
                        ("model", RandomForestClassifier(
                            n_estimators=400, random_state=42, class_weight="balanced"))])
clf_rf.fit(Xc_train, yc_train)
pred_rf_c = clf_rf.predict(Xc_test)

print("=== Clasificación binaria (OMS 15 µg/m³) - Random Forest ===")
print(classification_report(yc_test, pred_rf_c, digits=3))
ConfusionMatrixDisplay(confusion_matrix(yc_test, pred_rf_c)).plot()
plt.title("Matriz de confusión - Binaria (RF)")
plt.show()

"""**Etiqueta MULTICLASE: 0: ≤15, 1: (15,25], 2: >25**"""

def bucket_pm25(v):
    if v <= 15: return 0
    elif v <= 25: return 1
    else: return 2

train_df["pm25_bucket"] = train_df["PM_25"].apply(bucket_pm25)
test_df["pm25_bucket"]  = test_df["PM_25"].apply(bucket_pm25)

Xm_train = train_df[num_cols + cat_cols]
ym_train = train_df["pm25_bucket"]
Xm_test  = test_df[num_cols + cat_cols]
ym_test  = test_df["pm25_bucket"]

clf_mc = Pipeline(steps=[("prep", preprocess),
                        ("model", RandomForestClassifier(
                            n_estimators=500, random_state=42, class_weight="balanced"))])
clf_mc.fit(Xm_train, ym_train)
pred_mc = clf_mc.predict(Xm_test)

print("=== Clasificación multiclase (≤15, 15–25, >25) - RF ===")
print(classification_report(ym_test, pred_mc, digits=3))
ConfusionMatrixDisplay(confusion_matrix(ym_test, pred_mc)).plot()
plt.title("Matriz de confusión - Multiclase (RF)")
plt.show()

