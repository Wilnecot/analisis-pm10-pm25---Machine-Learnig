plt.figure(figsize=(5,5))
plt.scatter(y_test, pred_xgb, alpha=0.6)
plt.xlabel("PM2.5 observado (µg/m³)")
plt.ylabel("PM2.5 predicho (µg/m³)")
plt.title("Regresión PM2.5 - XGBoost")
